package numbers;

public class OneFor 
{
	public static void main(String[] args) 
	{
for(int i = 0; i < 11; i++)
	System.out.print(i+" ");
System.out.print('\n');

for(int i = -10; i < 21; i++)
	System.out.print(i+" ");
System.out.print('\n');

for(int i = -30; i < 12; i++)
	System.out.print(i+" ");
System.out.print('\n');

for(int i = 0; i < 51; i++)
	if(i%3 == 0)System.out.print(i+" ");
System.out.print('\n');

for(int i = 0; i < 101; i++)
	if(i%5 == 0 && i%7 == 0)System.out.print(i+" ");
	}
}
